package cocktail;

public class Cup {
    private int volume;
    private int calories;

    public Cup(int volume, int calories) {
        this.volume = volume;
        this.calories = calories;
    }

    public int getVolume() {
        return volume;
    }

    public int getCalories() {
        return calories;
    }
}
